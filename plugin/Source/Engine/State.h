    inline PersistentState createDefaultPersistentState()
    {
        PersistentState s;
        s.zones = createDefaultZones();
        return s;
    }

    inline std::vector<Ball> createBalls(const PersistentState& p)
    {
        float speedFactor = p.speed / 100.0f;
        std::vector<Ball> balls;
        for (int i = 0; i < p.ballCount; i++)
        {
            float angle = (3.14159265f * 2.0f * i) / p.ballCount + 3.14159265f * 0.25f;
            float baseSpeed = 0.003f + speedFactor * 0.007f;
            Ball b;
            b.x = 0.5f;
            b.y = 0.35f;
            b.vx = std::cos(angle) * baseSpeed;
            b.vy = std::sin(angle) * baseSpeed;
            balls.push_back(b);
        }
        return balls;
    }

    inline TransientState createTransientState(const PersistentState& p)
    {
        TransientState t;
        t.balls = createBalls(p);
        t.zoneFlash.resize(p.zones.size(), 0.0f);
        t.zoneLockout.resize(p.zones.size(), 0.0f);
        t.nextPlacementOrder = static_cast<int>(p.zones.size());
        return t;
    }

    inline int nextColorIndex(const std::vector<Zone>& zones)
    {
        if (zones.empty()) return 0;
        int maxIdx = -1;
        for (auto& z : zones)
            maxIdx = std::max(maxIdx, z.colorIndex);
        return (maxIdx + 1) % 16;
    }
