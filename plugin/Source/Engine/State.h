struct Zone
{
    float cx, cy;       // center (0-1 normalized)
    float hw, hh;       // half-width, half-height
    int midi;           // MIDI note number
    int colorIndex;
    int placementOrder = 0; // order in which zone was placed (for arp first-placed mode)
};

struct Ball
{
    float x, y;
    float vx, vy;
    std::vector<std::pair<float,float>> trail;
};

// Persistent state — serializable, maps to DAW parameters
struct PersistentState
{
    int gravity      = 20;    // 0-100
    int bounce       = 90;    // 50-100
    int friction     = 4;     // 0-50
    int speed        = 40;    // 5-100
    int ballCount    = 2;     // 0-16
    int ballSize     = 7;     // 3-18
    int minEnergy    = 25;    // 0-80
    bool solidZones  = true;
    bool ballCollide = false;
    int gateTime     = 250;   // ms (20-2000)

    int momentum     = 0;     // 0-100
    int jitter       = 0;     // 0-100

    int fanAmount    = 0;     // 0-100
    int fanSpeed     = 30;    // 0-100
    int fanDirection = 0;     // 0=N, 1=S, 2=E, 3=W, 4=Random

    int scaleRoot    = 0;     // 0-11
    int scaleName    = 0;     // index into getAllScales()

    int randomPitch        = 0;   // 0-100
    int randomOctaveChance = 0;   // 0-100
    int randomOctaveAmount = 1;   // 1-3
    int randomVelocity     = 0;   // 0-100
    int velocityFloor      = 10;  // 0-100 minimum velocity

    // Arpeggiator
    bool arpEnabled    = false;
    bool arpSync       = true;     // true = sync to host BPM divisions
    int  arpDivision   = 5;        // index into ArpDivision enum (default 1/8)
    int  arpRateMs     = 200;      // free-running rate in ms (10-4000)
    int  arpPlayMode   = 1;        // index into ArpPlayMode enum (default Up)
    bool arpPendulum   = false;
    int  arpRatchet    = 0;        // 0-16
    bool arpUseRandomization = false;

    std::vector<Zone> zones;
};

// Arp runtime state
struct ArpState
{
    int sequencePos = 0;
    int direction = 1;          // 1=forward, -1=backward (pendulum)
    int ratchetCount = 0;
    double sampleAccumulator = 0.0;  // sample-accurate timing
    std::vector<int> sequence;       // sorted zone indices
    int lastZoneCount = 0;
    bool needsRebuild = true;
};

// Transient state — runtime only
struct TransientState
{
    bool running = true;
    std::vector<Ball> balls;
    std::vector<float> zoneFlash;
    std::vector<float> zoneLockout;
    int selectedZone = -1;
    PRNG prng{42};
    float tickCount = 0.0f;
    int nextPlacementOrder = 0;
    ArpState arpState;
};

// MIDI event generated by physics
struct PhysicsEvent
{
    int midi;
    float velocity;   // 0-1
    int zoneIndex;
};

